using Microsoft.AspNetCore.Mvc.Infrastructure;
using Microsoft.ILP.Web.Services;
using Microsoft.ILP.Web.Settings;
using System.Runtime;

namespace Microsoft.ILP.Web
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();

            builder.Services.AddSingleton(builder.Configuration.GetSection("UserServiceEndpoints").Get<UserServiceEndpoints>());

            //HttpClinet            
            builder.Services.AddHttpClient<IUserService, UserService>("UserService");

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseRouting();

            app.UseAuthorization();

            app.MapStaticAssets();
            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}")
                .WithStaticAssets();

            app.Run();
        }
    }
}
